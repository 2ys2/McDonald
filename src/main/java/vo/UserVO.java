package vo;

public class UserVO {
	
	private String name;
	private int no;
	
	public UserVO() {	}
	
	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}
	

}
